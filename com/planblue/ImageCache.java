import java.awt.image.BufferedImage;
import java.util.Map;
import java.util.HashMap;
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////
class ImageCache {
    static Map <String, BufferedImage> cache = new HashMap <> ();
    private ImageCache() {};
    static void put( String key ){
        put( key, ImageUtil.load( ImageUtil.getImagePath( key )));
    }
    static void put( String key, BufferedImage image ){
        cache.put( key, image );
    }
    static BufferedImage get( String key ){
        return cache.get( key );
    }
}
