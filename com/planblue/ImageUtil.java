package com.planblue;
////////////////////////////////////////////////////////////////
import java.awt.image.BufferedImage;
////////////////////////////////////////////////////////////////
import java.io.File;
import java.io.IOException;
////////////////////////////////////////////////////////////////
import javax.imageio.ImageIO;
////////////////////////////////////////////////////////////////
class ImageUtil {
    private ImageUtil() {}
    static String imagesDirectory;
    static String imagesSuffix;
    static BufferedImage load( String path ){
        try {
            return ImageIO.read( new File( path ));
        } catch( IOException e ){
            e.printStackTrace();
            return null;
        }
    }
    static String getImagePath( String name ){
        return( imagesDirectory + "/" + name +
                "." + imagesSuffix );
    }
}
