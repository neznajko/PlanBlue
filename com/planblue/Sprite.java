package com.planblue;
////////////////////////////////////////////////////////////////
import java.awt.image.BufferedImage;
import java.awt.Graphics;
////////////////////////////////////////////////////////////////
abstract class Sprite {
    int x;
    int y;
    int width;
    int height;
    boolean visible;
    BufferedImage image;
    ////////////////////////////////////////////////////////////
    Sprite( int x, int y, BufferedImage image ){
        this.x = x;
        this.y = y;
        this.image = image;
        width = this.image.getWidth();
        height = this.image.getHeight();
        visible = false;
    }
    void draw( Graphics g ){
        if( visible ){
            g.drawImage( image, x, y, null );
        }
    }
    void move( int dx, int dy ){
        x += dx;
        y += dy;
    }
    boolean outOfCanvas() {
        if( y + height <= 0 )      return true;
        if( y >= PlanBlue.HEIGHT ) return true;
        return false;
    }
    int cx() {
        return x + width/ 2;
    }
    int cy() {
        return y + height/ 2;
    }
    boolean collideWith( Sprite sprite ){
        if(!( visible && sprite.visible )) return false;
        int distx = Math.abs( cx() - sprite.cx());
        int disty = Math.abs( cy() - sprite.cy());
        if( distx < ( width  + sprite.width  )/ 2 &&
            disty < ( height + sprite.height )/ 2 ){
            return true;
        }
        return false;
    }

}
