class Bullet extends Sprite {
    Bullet( String name ){
        super( 0, 0, ImageCache.get( name ));
    }
    @Override
    void move( int dx, int dy ){
        super.move( dx, dy );
        if( outOfCanvas()){
            visible = false;
        }
    }
}
